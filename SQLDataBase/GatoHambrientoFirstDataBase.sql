CREATE TABLE Inventario(
Id_Inventario INT PRIMARY KEY AUTO_INCREMENT,
Nombre VARCHAR(100) NOT NULL,
Stock Int NOT NULL,
Informacion varchar(255),
Costo DECIMAL(10,2) ,
status VARCHAR(20) not null,
IC_Id INT,
FOREIGN KEY (IC_Id) References Inventario_Categorias(Id_IC)
);

